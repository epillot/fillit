#include "fillit.h"

int		is_valid(char *buf)
{
	int i;
	int count;

	i = -1;
	count = 0;
	while (buf[++i] != '\0')
	{
		if ((i + 1) % 5 != 0 && i != 20)
		{
			if (buf[i] != '.' && buf[i] != '#')
				return (0);
		}
		else
		{
			if (buf[i] != '\n')
				return (0);
		}
		if (buf[i] == '#')
			count++;
	}
	return (count != 4 ? 0 : 1);
}
